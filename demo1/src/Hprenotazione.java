public class Hprenotazione {
    private String dataModifica = "";
    private String oraModifica = "";
    private String tipoModifica = "";

    public String getDataModifica() {
        return dataModifica;
    }

    public void setDataModifica(String dataModifica) {
        this.dataModifica = dataModifica;
    }

    public String getOraModifica() {
        return oraModifica;
    }

    public void setOraModifica(String oraModifica) {
        this.oraModifica = oraModifica;
    }

    public String getTipoModifica() {
        return tipoModifica;
    }

    public void setTipoModifica(String tipoModifica) {
        this.tipoModifica = tipoModifica;
    }
}
